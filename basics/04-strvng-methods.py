# string methods

# len()
course = "Python for Beginner's"

print(len(course))

print(course.upper())

print(course.lower())

print(course.title())

print(course.find("o"))     # returns index of character

print(course.replace("Beginner's", "Experts"))

# "in" operator, checking if something is found within a variable
print("Python" in course)      # will return a boolean answer 